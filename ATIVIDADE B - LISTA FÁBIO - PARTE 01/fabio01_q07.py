#7. Leia 3 números, calcule e escreva a soma dos 2 primeiros e
#a diferença entre os 2 últimos.



def main():

    num1 = int(input('Digite o primeiro número: '))
    num2 = int(input('Digite o segundo número: '))
    num3 = int(input('Digite o terceiro número: '))


    soma = num1 + num2
    diferenca = num2 - num3


    print('A soma dos dois primeiros números é igual a ',soma)
    print('A diferença dos dois últimos números é igual a ',diferenca)


main()


    
