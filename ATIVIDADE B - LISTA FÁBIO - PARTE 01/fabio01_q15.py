#15. Leia o valor da base e altura de um triângulo, calcule e escreva sua área.
#(área=(base * altura)/2).



def main():


    base = float(input('Digite a base de um triângulo: '))
    altura = float(input('Digite a altura de um triângulo: '))

    area = (base * altura) / 2


    print('A área do triângulo é %.1f'% area)



main()


    
