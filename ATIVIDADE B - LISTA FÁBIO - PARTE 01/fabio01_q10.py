#10. Leia 2 números inteiros, calcule e escreva o quociente e
#o resto da divisão do 1º pelo 2º.


def main():


    num1 = int(input('1° número: '))
    num2 = int(input('2° número: '))


    quociente = num1 // num2
    resto = num1 % num2


    print('O quociente da divisão é ',quociente,'e o resto é ',resto)



main()


    
